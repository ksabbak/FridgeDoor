import UIKit

extension UIColor {
    
    public class func appLightBlueColor() -> UIColor {
        
        return UIColor(red: CGFloat(0x3b) / 255.0, green: CGFloat(0x88) / 255.0, blue: CGFloat(0xa8) / 255.0, alpha: 1.0)
        
    }
    
    public class func appBrownColor() -> UIColor {
        
        return UIColor(red: CGFloat(0x6d) / 255.0, green: CGFloat(0x43) / 255.0, blue: CGFloat(0x33) / 255.0, alpha: 1.0)
        
    }
    
    public class func appWineColor() -> UIColor {
        
        return UIColor(red: CGFloat(0x91) / 255.0, green: CGFloat(0x16) / 255.0, blue: CGFloat(0x33) / 255.0, alpha: 1.0)
        
    }
    
    public class func appDarkBlueColor() -> UIColor {
        
        return UIColor(red:0.012, green:0.294, blue:0.420, alpha:1.00)
        
    }
    
    public class func appVeryLightBlueColor() -> UIColor {
        
        return UIColor(red:0.624, green:0.769, blue:0.827, alpha:1.00)
               
    }

    
}
